// ========= UTILITIES =========
function enhancedNormalize(text) {
  return text.toLowerCase()
    .replace(/[^\w\s]/gi, "")
    .replace(/\b(?:a|an|the|do|does|did|have|has|had|is|are|was|were|be|been|being)\b/gi, "")
    .replace(/\b(?:will|shall|would|should|can|could|may|might|must)\b/gi, "")
    .replace(/\s+/g, " ")
    .trim();
}

export function levenshtein(a, b) {
  const m = a.length, n = b.length;
  const dp = Array.from({ length: m + 1 }, () => Array(n + 1).fill(0));
  for (let i = 0; i <= m; i++) dp[i][0] = i;
  for (let j = 0; j <= n; j++) dp[0][j] = j;
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      if (a[i - 1] === b[j - 1]) dp[i][j] = dp[i - 1][j - 1];
      else dp[i][j] = 1 + Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1]);
    }
  }
  return dp[m][n];
}

// Typo-tolerant keyword matcher
function fuzzyMatch(word, keyword) {
  if (word === keyword) return true;
  if (word.length <= 3) return false;
  
  // Safe edit distance threshold: distance <= 1 for words longer than 3 characters
  const dist = levenshtein(word, keyword);
  if (dist <= 1) return true;
  
  // Substring or plural/singular containment match
  if (keyword.length > 4 && (word.startsWith(keyword) || keyword.startsWith(word))) {
    return true;
  }
  
  return false;
}

