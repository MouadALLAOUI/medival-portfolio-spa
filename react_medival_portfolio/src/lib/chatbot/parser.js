export function initializeSentenceChunks() {
  const chunks = [];
  knowledgeClusters.forEach(cluster => {
    cluster.sentences.forEach((sentence, index) => {
      chunks.push({
        text: sentence.trim(),
        paragraphIndex: index,
        isProfile: true,
        clusterId: cluster.id
      });
    });
  });
  return chunks;
}

export function generatePseudoLLMAnswer(question, sentenceChunks = [], conversationHistory = []) {
  const queryNormalized = enhancedNormalize(question);
  const qWords = queryNormalized.split(" ");

  // Quick Greetings check
  const GREETINGS_KEYWORDS = ["hello", "hi", "hey", "greetings", "bonjour", "salut", "hola", "yo", "welcome"];
  if (qWords.some(w => GREETINGS_KEYWORDS.includes(w))) {
    return {
      text: "Greetings, traveler! I am the Oracle, a keeper of Mouad's coding chronicles. Ask me about his origins, studies, skills, cybersecurity research, philosophy, projects, or hobbies!",
      intent: "greeting",
      confidence: 1.0,
      matches: 1,
      contextId: "general"
    };
  }

  // Quick Help check
  const HELP_KEYWORDS = ["help", "question", "ask", "can you", "what can you", "commands", "options"];
  if (qWords.some(w => HELP_KEYWORDS.includes(w))) {
    return {
      text: "I can recount Mouad's tales from his origins in Morocco, his educational path, Full-Stack skills, cybersecurity audits, programming philosophy, or car hobbies. Speak your query, and I shall unveil the scrolls.",
      intent: "help",
      confidence: 1.0,
      matches: 1,
      contextId: "general"
    };
  }

  // Track the active topic context from the stateful history (Multi-Turn Conversational Memory)
  let previousContextId = null;
  for (let i = conversationHistory.length - 1; i >= 0; i--) {
    if (conversationHistory[i].role === 'bot' && conversationHistory[i].contextId) {
      previousContextId = conversationHistory[i].contextId;
      break;
    }
  }

  // Evaluate and score each topic cluster
  const scores = knowledgeClusters.map(cluster => {
    let score = 0;
    let keywordMatches = 0;

    qWords.forEach(qWord => {
      if (qWord.length <= 2) return;

      // 1. Fuzzy Match keywords
      const hasKeywordMatch = cluster.keywords.some(keyword => fuzzyMatch(qWord, keyword));
      if (hasKeywordMatch) {
        score += 3.0; // High score multiplier for keyword matches
        keywordMatches++;
      }

      // 2. Incremental sentence match
      cluster.sentences.forEach(sentence => {
        const sNormalized = enhancedNormalize(sentence);
        if (sNormalized.split(" ").includes(qWord)) {
          score += 0.4;
        }
      });
    });

    // 3. Apply Contextual weight bonus for Conversational Memory
    if (previousContextId && cluster.id === previousContextId) {
      score += 1.5;
    }

    return {
      cluster,
      score,
      keywordMatches
    };
  });

  // Sort by highest score desc
  scores.sort((a, b) => b.score - a.score);

  const bestMatch = scores[0];

  // Set match threshold
  if (!bestMatch || bestMatch.score < 0.4) {
    return {
      text: "I'm afraid my scrolls contain no specific wisdom on that topic. Perhaps rephrase your inquiry to ask about Mouad's skills, origins, education, projects, or hobbies?",
      intent: "unknown",
      confidence: 0.0,
      matches: 0,
      contextId: previousContextId || "general"
    };
  }

  // Cohesive Sequential Synthesis from within the winning cluster block
  const winningCluster = bestMatch.cluster;
  const synthesizedResponse = winningCluster.sentences.join(" ");

  return {
    text: synthesizedResponse,
    intent: winningCluster.id,
    confidence: bestMatch.score,
    matches: winningCluster.sentences.length,
    contextId: winningCluster.id
  };
}
