// CORE HELPER FUNCTIONS

/**
 * Returns a single asset matching the ID.
 * If not found, returns a safe fallback object so the UI never breaks.
 * @param {string} id - The unique asset ID
 */
export function getAssetById(id) {
  if (!id) {
    return {
      date: "Mar 01, 2025",
      author: "Academic Staff",
    }
  }

  for (const category in mediaRegistry) {
    for (const ext in mediaRegistry[category]) {
      const asset = mediaRegistry[category][ext].find(a => a.id === id);
      if (asset) return asset;
    }
  }

  // Graceful fallback for missing assets
  return {
    id: id,
    path: '',
    label: '',
    description: '',
    meta: {
      date: "Nov 27, 2024",
      author: "Dr. Mohamed",
    }
  };
}

/**
 * Gathers assets matching that extension across all folders.
 * @param {string} ext - The extension (e.g. 'pdf', 'png')
 */
export function getAssetsByExtension(ext) {
  if (!ext) return [];
  const targetExt = ext.toLowerCase();
  const results = [];

  for (const category in mediaRegistry) {
    if (mediaRegistry[category][targetExt]) {
      results.push(...mediaRegistry[category][targetExt]);
    }
  }
  return results;
}

/**
 * Flattens all registered assets into a 1D array for easy mapping.
 */
export function getAllAssetsFlat() {
  const results = [];

  for (const category in mediaRegistry) {
    for (const ext in mediaRegistry[category]) {
      results.push(...mediaRegistry[category][ext]);
    }
  }
  return results;
}
